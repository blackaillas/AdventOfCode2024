const LINE_SEP = '\n';

export function solvePart1() {
    let sum = 0;
    const matrix = getMatrix();

    // ROWS
    for (let i = 0; i < matrix.length; i++) {
        const row = matrix[i].join('');
        sum += countOfSearchString(row);
    }

    // COLS
    for (let i = 0; i < matrix[0].length; i++) {
        const col = matrix.map(row => row[i]).join('');
        sum += countOfSearchString(col);
    }

    // DIAGONALS TOPLEFT TO BOTTOMRIGHT, HORIZONTAL
    const WORD_MIN_LENGTH_DELTA = 4;
    let diag = '';
    for (let i = matrix[0].length - WORD_MIN_LENGTH_DELTA; i >= 0; i--) {
        for (let j = 0; j < matrix[0].length - (i); j++) {
            diag += matrix[0 + j][i + j];
        }
        sum += countOfSearchString(diag);
        diag = '';
    }
    // DIAGONALS TOPLEFT TO BOTTOMRIGHT, VERTICAL
    for (let i = 1; i <= matrix.length - WORD_MIN_LENGTH_DELTA; i++) {
        for (let j = 0; j < matrix.length - (i); j++) {
            diag += matrix[i + j][0 + j];
        }
        sum += countOfSearchString(diag);
        diag = '';
    }
    // DIAGONALS BOTTOMLEFT TO TOPRIGHT, HORIZONTAL
    for (let i = matrix[0].length - WORD_MIN_LENGTH_DELTA; i >= 0; i--) {
        for (let j = 0; j < matrix[0].length - (i); j++) {
            diag += matrix[matrix.length - 1 - j][i + j];
        }
        sum += countOfSearchString(diag);
        diag = '';
    }
    // DIAGONALS BOTTOMLEFT TO TOPRIGHT, VERTICAL
    for (let i = matrix.length - 2; i >= WORD_MIN_LENGTH_DELTA - 1; i--) {
        for (let j = 0; j <= i; j++) {
            diag += matrix[i - j][0 + j];
        }
        sum += countOfSearchString(diag);
        diag = '';
    }

    return sum;
}

export function solvePart2() {
    let sum = 0;
    const matrix = getMatrix();

    for (let i = 1; i < matrix.length - 1; i++) {
        for (let j = 1; j < matrix[0].length - 1; j++) {
            if (middleCharOk(matrix[i][j]) && leftTopRightBottomDiagonalOk(matrix, i, j) && leftBottomRightTopDiagonalOk(matrix, i, j)) {
                sum++;
            }
        }
    }

    return sum;
}

function middleCharOk(char) {
    return char === 'A';
}

function leftTopRightBottomDiagonalOk(matrix, centerI, centerJ) {
    return (matrix[centerI - 1][centerJ - 1] === 'M' && matrix[centerI + 1][centerJ + 1] === 'S') ||
        (matrix[centerI - 1][centerJ - 1] === 'S' && matrix[centerI + 1][centerJ + 1] === 'M');
}

function leftBottomRightTopDiagonalOk(matrix, centerI, centerJ) {
    return (matrix[centerI - 1][centerJ + 1] === 'M' && matrix[centerI + 1][centerJ - 1] === 'S') ||
        (matrix[centerI - 1][centerJ + 1] === 'S' && matrix[centerI + 1][centerJ - 1] === 'M');
}

function getMatrix() {
    const lines = input.split(LINE_SEP);
    const matrix = [];
    for (let i = 0; i < lines.length; i++) {
        matrix.push(lines[i].split('').map(String));
    }
    return matrix;
}

function countOfSearchString(string) {
    let count = 0;
    count += string.match(/XMAS/g)?.length ?? 0;
    count += string.match(/SAMX/g)?.length ?? 0;
    return count;
}

