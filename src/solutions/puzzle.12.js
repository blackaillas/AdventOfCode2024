function addCellToArea(matrix, areas, sign, i, j) {
    const similarAreas = areas[sign].areas;
    let neighborCellGroupId = undefined;
    for (let z = 0; z < similarAreas.length; z++) {
        const group = similarAreas[z];
        if (group.cells.some(cell => Math.abs(cell.row - i) <= 1 && Math.abs(cell.col - j) <= 1)) {
            neighborCellGroupId = group.group;
            break;
        }
    }

    if (neighborCellGroupId) {
        const found = similarAreas.find(x => x.group === neighborCellGroupId);
        found.size++;
        found.fence += countFence(matrix, i, j);
        similarAreas.find(x => x.group === neighborCellGroupId).cells.push({ row: i, col: j });
    } else {
        similarAreas.push({ group: similarAreas.length + 1, size: 1, fence: countFence(matrix, i, j), cells: [{ row: i, col: j }] });
    }
}

function flatGroups(areas) {
    let countOfMerge = 0;
    Object.keys(areas).forEach(area => {
        for (let index = 0; index < areas[area].areas.length; index++) {
            const group = areas[area].areas[index];
            if (group.merged) {
                continue;
            }
            const otherGroups = areas[area].areas.filter(x => x.group !== group.group && !x.merged);
            for (let i = 0; i < otherGroups.length; i++) {
                const otherGroup = otherGroups[i];
                if (group.cells.some(cell => otherGroup.cells.some(otherCell => Math.abs(cell.row - otherCell.row) <= 1 && Math.abs(cell.col - otherCell.col) <= 1))) {
                    console.debug('Merging groups', group.group, otherGroup);
                    group.size += otherGroup.size;
                    group.fence += otherGroup.fence;
                    group.cells = [...group.cells, ...otherGroup.cells];
                    otherGroup.merged = true;
                    i++;
                    // areas[area].areas = areas[area].areas.filter(x => x.group !== otherGroup.group);
                    countOfMerge++;
                }
            }
        }
    });
    return countOfMerge;
}

function countFence(matrix, i, j) {
    let fence = 0;

    if (i === 0 || matrix[i - 1][j] !== matrix[i][j]) {
        fence++;
    }
    if (i === matrix.length - 1 || matrix[i + 1][j] !== matrix[i][j]) {
        fence++;
    }
    if (j === 0 || matrix[i][j - 1] !== matrix[i][j]) {
        fence++;
    }
    if (j === matrix[i].length - 1 || matrix[i][j + 1] !== matrix[i][j]) {
        fence++;
    }

    return fence;
}

